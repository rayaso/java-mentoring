package SorayaQuiz;

public class EncapsulationUsernameAndPassword {
    private int ssn;
    private int acc;

    public int getSsn() {
        return ssn;
    }

    public void setSsn(int ssn) {
        this.ssn = ssn;
    }

    public int getAcc() {
        return acc;
    }

    public void setAcc(int acc) {
        this.acc = acc;
    }

}
